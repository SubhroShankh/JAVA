public class Escape {
     public static void main(String[] args){
        System.out.println("Hello\"subhro shankh\"...");
        // we use \ to escape the double quotes

        System.out.println("Hello\\subhro shankh...");
        // we use \\ to escape the backslash

        System.out.println("Good Morning\nsubhro shankh...");
        // we use \n to create a new line
        
        System.out.println("Hello\tWorld");
        // we use \t to create a tab space

        System.out.println("Hello\b World");
        // we use \b to create a backspace
     }
}
